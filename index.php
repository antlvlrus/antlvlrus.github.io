<h1 class="uk-article-title"
 style="margin: 0px 0px 20px; font-family: &quot;Open Sans&quot;; font-weight: 300; line-height: 42px; color: rgb(68, 68, 68); font-size: 36px; text-transform: none; font-style: normal; letter-spacing: normal; orphans: 2; text-indent: 0px; white-space: normal; widows: 2; word-spacing: 0px; background-color: rgb(255, 255, 255); text-align: center;">Какое
питание необходимо для здоровья человека</h1>
<p class="uk-article-meta"
 style="margin: 20px 0px; font-size: 12px; line-height: 18px; color: rgb(153, 153, 153); font-family: Arial,Helvetica,sans-serif; font-style: normal; font-weight: 400; letter-spacing: normal; orphans: 2; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: 2; word-spacing: 0px; background-color: rgb(255, 255, 255);">Дата
